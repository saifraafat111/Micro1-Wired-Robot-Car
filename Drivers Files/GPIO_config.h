#ifndef BIT_MATH_H_
#define BIT_MATH_H_

/* =====================================================================
 *  BIT_MATH.h — MACROs للعمليات الـ Bit-level على الرجسترات
 *
 *  بنستخدمها في أي مكان بنحتاج فيه نلعب على bit واحد بس داخل register
 *  من غير ما نأثّر على باقي البيتس.
 *
 *  الفكرة:
 *    1 << BIT     →  بنعمل قناع (mask) فيه 1 واحدة في مكان البِت المطلوب.
 *    REG |= mask  →  set:    خلّي البِت = 1 ، الباقي زي ما هو.
 *    REG &= ~mask →  clear:  خلّي البِت = 0 ، الباقي زي ما هو.
 *    REG ^= mask  →  toggle: اعكس قيمة البِت ، الباقي زي ما هو.
 *    (REG >> BIT) & 1 → get: استخرج قيمة البِت (0 أو 1).
 * ===================================================================== */

#define Set_Bit(REG,BIT)    (REG |=  (1 << BIT))   /* خلّي البِت = 1 */
#define Clear_Bit(REG,BIT)  (REG &= ~(1 << BIT))   /* خلّي البِت = 0 */
#define Tog_Bit(REG,BIT)    (REG ^=  (1 << BIT))   /* اعكس البِت */
#define Get_Bit(REG,BIT)    ((REG >> BIT) & 1)     /* اقرا البِت */

#endif /* BIT_MATH_H_ */
